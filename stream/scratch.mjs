const parsed = lines.map(line => {
  const lineStartMatch = line.match(/^\[([^\]]+)\]/);
  if (!lineStartMatch) return null;
  const lineStart = timeToSec(lineStartMatch[1]);
  
  let textPart = line.substring(lineStartMatch[0].length);
  const words = [];
  
  let regex = /([^<]*)(?:<([^>]+)>)?/g;
  let match;
  let currentStartTime = lineStart;
  
  while ((match = regex.exec(textPart)) !== null) {
      if (match[1] === '' && match[2] === undefined) break;
      if (!match[1] && match[2]) continue;
      let text = match[1] || "";
      let endTimeStr = match[2];
      if (text) {
          let endTime = endTimeStr ? timeToSec(endTimeStr) : (currentStartTime + 0.5); // guess end time if none
          words.push({ text: text, start: currentStartTime, end: endTime });
          currentStartTime = endTime;
      }
  }
  
  return { time: lineStart, text: words.map(w => w.text).join(''), words };
}).filter(Boolean);

